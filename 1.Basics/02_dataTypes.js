// there are 7 types of premitive data types in JavaSript
// 1.String
// 2.Number
// 3.Boolean
// 4.symbol => uniqe
// 5. BigInt
// 6.undefined
// 7.null
// String data type
let firstName = 'saman';
console.log(firstName); // Output: saman

// Number data type
let age = 25;
console.log(age); // Output: 25

// Boolean data type
let isStudent = true;
console.log(isStudent); // Output: true

// Null data type
let job = null;
console.log(job); // Output: null

// Undefined data type
let car;
console.log(car); // Output: undefined

// Object data type
let person = {
    name: 'saman',
    age: 25
};
console.log(person); // Output: { name: 'saman', age: 25 }

// Array data type
let colors = ['red', 'green', 'blue'];
console.log(colors); // Output: ['red', 'green', 'blue']


